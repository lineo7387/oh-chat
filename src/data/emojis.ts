const RECENT_EMOJIS_KEY = 'oh-chat:recent-emojis'
const MAX_RECENT = 24

export function getRecentEmojis(): string[] {
  try {
    const raw = localStorage.getItem(RECENT_EMOJIS_KEY)
    if (raw) {
      const parsed = JSON.parse(raw) as string[]
      if (Array.isArray(parsed)) return parsed
    }
  } catch {
    // ignore
  }
  return []
}

export function addRecentEmoji(emoji: string) {
  const recent = getRecentEmojis()
  const filtered = recent.filter((e) => e !== emoji)
  filtered.unshift(emoji)
  const trimmed = filtered.slice(0, MAX_RECENT)
  localStorage.setItem(RECENT_EMOJIS_KEY, JSON.stringify(trimmed))
}

export function searchEmojis(query: string): { category: string; emoji: string }[] {
  const q = query.toLowerCase().trim()
  if (!q) return []

  const results: { category: string; emoji: string }[] = []
  for (const cat of emojiCategories) {
    for (const emoji of cat.emojis) {
      if (emoji.includes(q) || cat.name.toLowerCase().includes(q)) {
        results.push({ category: cat.name, emoji })
      }
    }
  }
  return results
}
